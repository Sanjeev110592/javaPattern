public class SolidRectangle {
    public static void main(String[] args){
        int m = 6 ; //Row
        int n = 10 ; //Column
        for(int i = 0 ; i < m ; i++){
            for(int j = 0 ; j < n ; j++){
                System.out.print("* ") ;
            }
            System.out.println() ;
        }
    }
}
